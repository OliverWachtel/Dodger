function enemy(x, y, r, blob) {
  this.pos = createVector(x, y);
  this.r = r;
  this.vel = createVector(0, 0);
  this.blob = blob;

  this.update = function() {
    var newvel = createVector(this.blob.pos.x - this.pos.x, this.blob.pos.y - this.pos.y);
    //stops Dodger
    // var newvel = this.blob.pos.sub(this.pos);
    newvel.setMag(2.25);
    this.vel.lerp(newvel, 0.2);
    this.pos.add(this.vel);
    
  };
  
  this.show = function() {
    fill(25);
    ellipse(this.pos.x, this.pos.y, this.r * 2, this.r * 2);
  };
}











